<center><h1>Refund/Cancellation Policy</h1></center>
<br>

<p>Consumer has to ensure that Transaction Reference Number has been generated and if the same has not been generated, she/he has to make the payment of bill afresh to avoid disconnection.
<br>
<br>
Refund of the money due to above unsuccessful transaction The consumer has to take up the matter with the respective issuer bank. Public Health Engineering Department will not be responsible for such refund.
<br>
<br>
There are three ways of transaction flow –

<ul> 
    <li>Amount is debited from consumer’s account and success message is shown in Public Health Engineering Department portal. Connection does not break.</li>
    <li>Connection breaks before amount is debited from user’s account. There should not be any issue as consumer’s bank/card account is not debited.</li>
    <li>Connection breaks after amount is debited from user’s account but payment status is not shown in Public Health Engineering Department portal. Successful payments will be settled during reconciliation process between Public Health Engineering Department and Designated Bank/Billdesk. The consumers can pay again to make sure that he/she gets transaction receipt. In case of double (or multiple payment) of same bill, the extra amount will be saved as credit. This will be adjusted in the next bills.</li>
    
</ul> 
</p>
